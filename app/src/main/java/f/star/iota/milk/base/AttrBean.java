package f.star.iota.milk.base;


public class AttrBean {
    private int width;
    private int height;

    public AttrBean(int width, int height) {
        this.width = width;
        this.height = height;
    }

    public int getWidth() {
        return width;
    }

    public int getHeight() {
        return height;
    }
}
